      empty.textContent = `æš‚æ— ${title}æ•°æ®ï¼Œåœ¨ç¬”è®°ä¸­ä½¿ç”¨ #${type === 'skills' ? 'skill' : type === 'equipment' ? 'equip' : 'dungeon'}-åˆ†ç±»-å†…å®¹ æ·»åŠ `;
